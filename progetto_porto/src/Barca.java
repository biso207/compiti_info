public class Barca {
    // attributi
    String nome, nazione, tipologia;
    int lunghezza, stazza;

    Barca(String nome, String nazione, String tipologia, int lunghezza, int stazza) {
        this.nome = nome;
        this.nazione = nazione;
        this.tipologia = tipologia;
        this.lunghezza = lunghezza;
        this.stazza = stazza;
    }


}
